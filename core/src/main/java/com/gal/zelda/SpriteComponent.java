package com.gal.zelda;

public class SpriteComponent {
    public int srcX;
    public int srcY;
    public int srcWidth;
    public int srcHeight;

    public SpriteComponent(int srcX, int srcY, int srcWidth, int srcHeight) {
        this.srcX = srcX;
        this.srcY = srcY;
        this.srcWidth = srcWidth;
        this.srcHeight = srcHeight;
    }
}
