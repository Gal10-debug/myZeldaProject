package com.gal.zelda;

public class RenderComponent {
    public float width;
    public float height;
    public float r;
    public float g;
    public float b;
    public float a;

    public RenderComponent(float width, float height, float r, float g, float b, float a) {
        this.width = width;
        this.height = height;
        this.r = r;
        this.g = g;
        this.b = b;
        this.a = a;
    }
}
